import Foundation
import UIKit

extension UIColor {
    
    func backgroundGrey() -> UIColor {
        return UIColor(red: 212/255.0, green: 212/255.0, blue: 212/255.0, alpha: 1.0)
    }
    
    func mintGreen() -> UIColor {
        return UIColor(red: 152/255.0, green: 225/255.0, blue: 152/255.0, alpha: 1.0)
    }
    
    func BeccaBlue() -> UIColor {
        return UIColor(red: 80/255.0, green: 116/255.0, blue: 150/255.0, alpha: 1.0)
    }
    
    
    
}